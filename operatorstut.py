#arithmetic operators
  #'+','-','*','**','/','//','%'
print(5+6)
print(5-6)
print(5/6)
print(5//6)
print(5%6)
print(5*6)
print(5**6)

#assignment operators
    #'=','+=','*=','-='etc.
x=5
print(x)
x+=5
print(x)
x*=2
print(x)

#comparison operators
    #'==','!=','<','>','<=','>=',etc
print(x==5)
print(x!=5)
print(x>1)
print(x<=5)

#logical operators
    #'and','or'
a=(x!=5)
b=(x==5)
print(a and b)
print(a or b)

#identity operator
    #'is','is not',
print(5 is 6)

#membership operator
    #'in','not in',
list=[1,2,3,4]
print(1 in list)
print(7 not in list)

#bitwise operators
    # 0 - 00
    # 1 - 01
    # 2 - 10
    # 3 - 11
print(0 & 2)#& means and
print(0 | 3)#| means or